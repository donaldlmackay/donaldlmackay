<h1>FTP to Transfer Files Between PC and SmartPhone</h1>

<p>There is FTP and quite a few other ways of transferring files. More details below. Other ways seem to be prone to niggles and inconsistencies.
</p>
<p>FTP (File Transfer Protocol) started in the early 1970s. So it is tried, tested and reasonable robust.
</p>
<p>It needs an FTP Server on Device A, and an FTP Client on Device B. Both the Server and the Client can be on any computer, big or small, powerful or low powered.
</p>
<p>Googling for FTP, the most common combination seemed to be FTP Client on a PC and FTP Server on a SmartPhone.
</p>
<p>A few assumptions. The PC is running Windows 10 or 7 or even XP. The SmartPhone is Running Android 5 or later. 
</br><a href="#iPhone">(See below for iPhone.)</a>
</p>
<p>It is easiest to use a stand-alone program on Windows: Filezilla - Open Source (free), widely used and has a long pedigree:-
</br><a href="http://www.snapfiles.com/get/filezilla.html">http://www.snapfiles.com/get/filezilla.html</a>
This page shows how to use it:-
</br><a href="http://support.hostgator.com/articles/how-to-transfer-files-with-filezilla">http://support.hostgator.com/articles/how-to-transfer-files-with-filezilla</a>
</p>
<p>It is easiest to use a stand-alone App on Android: FTP Server (by Banana Studio):-
</br><a href="https://play.google.com/store/apps/details?id=net.xnano.android.ftpserver ">https://play.google.com/store/apps/details?id=net.xnano.android.ftpserver </a>
</p>
<p>There are lots of Android FTP Servers. This one has helpful screen snapshots on its google play page.
</p>
<p>Using part of Windows to do FTP, it presupposes a lot of Windows knowledge, and trying to use part of another Android App is confusing. Many results from a google search will not use a stand-alone App or program so need a good background understanding.
</p>

<h2>Setting up the server</h2>

<p>This is client-server so it will need to logon with a username and password. Part of setting up FTP Server requires creating a username and password.</p>
<p>The first part of this how-to page shows  "How To Setup FTP Server in Android To Share files":-
</br><a href="https://www.how2shout.com/how-to/setup-ftp-server-in-android-to-share-files.html">https://www.how2shout.com/how-to/setup-ftp-server-in-android-to-share-files.html</a>
</p>
<p>The second part "Access the Android FTP server from Laptop or Personal Desktop PC Without any FTP client" 
needs a good understanding of windows.
</p>
<p>The third part "Access Android Phone FTP server wirelessly by another Android Smartphone using the EFSfileExplorer app" is not using a stand-alone Android App.
</p>
<h2>Setting up the client</h2>

<p>This page shows how to run FileZilla:-
</br><a href="https://www.softwarert.com/ftp-file-transfer-android-pc-es/">https://www.softwarert.com/ftp-file-transfer-android-pc-es/</a>
Start from about half way down "Secondly we need to run the FTP software on computer to access the FTP server address above. Run FileZilla or any other FTP tool you have". [The first part is the non stand-alone App]
</p>

<h2>Other techniques</h2>

<p>"There are various ways for exchanging data between a PC and a mobile phone. Basically you can transfer files through USB connection, Bluetooth, Wi-Fi, hotspot, these are the four methods you can use to copy your data over between different devices for now." This quote is from the <a href="https://www.softwarert.com/ftp-file-transfer-android-pc-es/">https://www.softwarert.com</a> page (above). All of them work to a greater or lesser extent. They have, with the exception of FTP, all caused grief at one time or another - hence FTP.
</p>

<h2>Other way round</h2>

<p>Use FTP server on the PC
</br><a href="http://www.snapfiles.com/get/quickeasyftpserver.html">http://www.snapfiles.com/get/quickeasyftpserver.html</a>
</p>
<p>and FTP client on Android
</br><a href="https://play.google.com/store/apps/details?id=com.ftpcafe.trial">https://play.google.com/store/apps/details?id=com.ftpcafe.trial</a>
</p>
<p>This works very well, but google doesn't show much on how to do it.
</p>
<p>For those who have been using FTP for a while and understand what is going on, this is well worth doing, but probably not for a first foray.
</p>
