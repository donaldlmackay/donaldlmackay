<h1>How to Insert an Image Inline in Yahoo! Mail</h1>
<a href="http://email.about.com/od/yahoomailtips/qt/How_to_Insert_an_Image_Inline_in_an_Email_with_Yahoo_Mail.htm">http://email.about.com/od/yahoomailtips/qt/How_to_Insert_an_Image_Inline_in_an_Email_with_Yahoo_Mail.htm</a><br>
<br>
&nbsp;&nbsp;&nbsp; Locate the image on a web site, or open it
directly using its URL (address).<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; You can upload the image
to your web host or use a graphic you found on the web (if you can
use it without violating any terms).<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; If you don't have a web
site, try a free image hosting service.<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; If your image is larger
than some 640x640 pixels, consider shrinking it to more handy
proportions before you put it online. <br>
&nbsp;&nbsp;&nbsp; Open the browser window with the image and an
email you are composing in Yahoo! Mail side by side.<br>
&nbsp;&nbsp;&nbsp; Drag and drop the image into the message. <br>
<br>
As an alternative to dragging and dropping, you can try copying and
pasting:<br>
<br>
&nbsp;&nbsp;&nbsp; Highlight the image - just the image - with your
mouse.<br>
&nbsp;&nbsp;&nbsp; Press Ctrl-C (Windows, Linux) or Command-C (Mac).<br>
&nbsp;&nbsp;&nbsp; Position the cursor where you want the image to
show up in an email you are composing in Yahoo! Mail.<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Make sure you have
enabled the Yahoo! Mail rich text editor. <br>
&nbsp;&nbsp;&nbsp; Hit Ctrl-V (Windows and Linux) or Command-V (Mac)
to paste the image.<br>
&nbsp;&nbsp;&nbsp; If you use an image uploaded to a free image
hosting service:<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Click on the image.<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Link the image to the
image's page address at the free image hosting service (something
resembling "http://img.imageshack.us/my.php?image=example.jpg" if
you use ImageShack, for example). <br>
<br>
&nbsp;&nbsp;&nbsp; Continue editing (and adding more images).<br>
&nbsp;&nbsp;&nbsp; Finally, send your Yahoo! Mail message as usual.

