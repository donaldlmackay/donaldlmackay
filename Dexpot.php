      <h2 class="schwarz">Quick Start Guide</h2>
      <h3 class="abstandunten">First steps with Dexpot</h3>
      <p class="abstandoben">
        <a name="conceptvirtualdesktops">
          <span class="control">1. The concept of virtual desktops</span>
        </a>
      </p>
      <p>Virtual desktops are supplemental work spaces for windows and icons in Windows. You can switch virtual desktops at a      keystroke or with your mouse.</p>
      <p class="abstandoben">
        <a name="switchdesktops">
          <span class="control">2. Switching desktops</span>
        </a>
      </p>
      <p>Run Dexpot (from your desktop). You&#39;ll see the Dexpot icon in your systray. Right-click the systray icon and choose      &#39;Desktop 2&#39; from the main menu.</p>
      <p class="bisskenabstandoben">
        <img src="http://www.dexpot.de/gfx/mainmenu_en.png" />
      </p>
      <p class="abstandoben">
        <a name="movewindows">
          <span class="control">3. Moving windows</span>
        </a>
      </p>
      <p>Click the title bar of a window with your right mouse button and choose &#39;Move &gt; Desktop 2&#39; from the folding      menu.</p>
      <p class="bisskenabstandoben">
        <img src="http://www.dexpot.de/gfx/systemmenus_en.png" />
      </p>
      <p class="abstandoben">
        <a name="copywindows">
          <span class="control">4. Copying windows</span>
        </a>
      </p>
      <p>Click the title bar of a window with your right mouse button and choose &#39;Copy &gt; Desktop 2&#39; from the folding      menu.</p>
      <p class="abstandoben">
        <a name="assignapps">
          <span class="control">5. Assigning applications</span>
        </a>
      </p>
      <p>Choose &#39;Assign applications&#39; from the main menu. Select a window of your choice in the list of running      applications. Assign this application to a desktop by using the combo box right of &#39;Assign application to&#39;.</p>
      <p class="bisskenabstandoben">
        <img class="siebenfenster" src="http://www.dexpot.de/gfx/appassignment_en.png" />
      </p>
      <p class="abstandoben">
        <a name="activateplugins">
          <span class="control">6. Activating plugins</span>
        </a>
      </p>
      <p>Choose &#39;Settings&#39; from the main menu. Switch to &#39;Plugins and Extras&#39;. Click the checkbox of the plugin you      would like to activate. Click the button &#39;Configure&#39; to customize the plugin.</p>
      <p class="abstandoben">
        <a name="setuphotkeys">
          <span class="control">7. Setup hotkeys</span>
        </a>
      </p>
      <p>Choose &#39;Settings&#39; from the main menu. Switch to &#39;Control&#39;. You find all hotkeys on tab &#39;Hotkeys&#39;.      Expand the hotkey group &#39;Switch desktop&#39; with a double-click. Click the button &#39;Define&#39;. That followed, press      the key on your keyboard you would like to use. If you wish, you can define an additional modificator like SHIFT.</p>
      <p class="bisskenabstandoben">
        <img class="siebenfenster" src="http://www.dexpot.de/gfx/hotkeys_en.png" />
      </p>
      <p class="abstandoben">
        <a name="desktopbackgrounds">
          <span class="control">8. Desktop backgrounds</span>
        </a>
      </p>
      <p>Choose &#39;Configure Desktops&#39; from the main menu. Switch to tab &#39;Background&#39;. Click the desktop in the left      desktop bar you would like to set a separate wallpaper. Confirm the prompt with &#39;Yes&#39;. Browse your computer for      images by clicking the thumbnail of the desired monitor.</p>
      <p class="bisskenabstandoben">
        <img class="siebenfenster" src="http://www.dexpot.de/gfx/background_monitor_en.png" />
      </p>
      <p class="abstandoben">
        <a name="autostart">
          <span class="control">9. Start-Up</span>
        </a>
      </p>
      <p>Choose &#39;Configure Desktops&#39; from the main menu. Switch to tab &#39;Start-Up&#39;. Click the desktop in the left      desktop bar you would like to set a start-up. A click on the button &#39;Browse...&#39; is enough to browse your computer for      the application you would like to start automatically. You need to specificy an event by choosing one from the combo box.</p>
      <p class="abstandoben">
        <a name="iconcustomizationseparate">
          <span class="control">10. Separate folders for each desktop (Default)</span>
        </a>
      </p>
      <p>Choose &#39;Settings&#39; from the main menu. Go to &#39;Switching Desktops&#39;. Enable the option &#39;Customize desktop       icons&#39;.
      <br />
      <i>Using this method of icon customization, Dexpot creates physically independent folders.</i>
      <br />
      <br />Please mind the following characteristics:</p>
      <ul class="presskit">
        <li>To backup the icon distribution, please save a profile via &#39;Settings &gt; General &gt; Profiles&#39;. If necessary,        you can restore the icon distribution using the hotkey &quot;Reload icons from profile&quot; from &#39;Settings &gt;        Controls &gt; Hotkeys &gt; Miscellaneous&#39;</li>
        <li>Icons stored in the public user folder are visible on all desktops. Furthermore, such icons do not appear in the icon        list of &#39;Desktop Windows&#39;</li>
        <li>The Windows function &quot;Align to grid&quot; can be set per desktop</li>
        <li class="fett">Applying the Windows function &quot;Auto arrange&quot; will arrange icons of         <u>all</u> desktops left-justified</li>
        <li><a href="http://dexpot.de/forum/viewtopic.php?f=21&amp;t=1839" target="_blank">[FAQ] How to place different icons on each          desktop</a></li>
      </ul>
      <p class="abstandoben">
        <a name="separatemoveicons">
          <span class="control">10.1 Moving icons</span>
        </a>
      </p>
      <p>Choose &#39;Desktop Manager&#39; from the main menu. Drag your icon you would like to move to the desired target desktop      in Desktop Manager.
      <br />
      <br />Tip: If you need to move many icons, choose &#39;Desktop Windows&#39; from the main menu. Change to tab      &#39;Icons&#39;. Select the icons you would like to move. Drag the icons to one of the desktops in the left desktop bar.</p>
      <p class="abstandoben">
        <a name="separatecopyicons">
          <span class="control">10.2 Copying icons</span>
        </a>
      </p>
      <p>Choose &#39;Desktop Manager&#39; from the main menu. While holding CTRL, drag your icon you would like to move to the      desired target desktop in Desktop Manager.
      <br />
      <br />Tip: If you need to copy many icons, choose &#39;Desktop Windows&#39; from the main menu. Change to tab      &#39;Icons&#39;. Select the icons you would like to copy. While holding CTRL, drag the icons to one of the desktops in the      left desktop bar.</p>
      <p class="abstandoben">
        <a name="separatedeleteicons">
          <span class="control">10.3 Deleting icons</span>
        </a>
      </p>
      <p>Right-click the icon on your desktop you would like to delete. Choose &#39;Delete&#39;. You will find the icon in the      recycle bin.</p>
      <p class="abstandoben">
        <a name="keeporiginalwindowsfolder">
          <span class="control">10.4 Keep original desktop folder</span>
        </a>
      </p>
      <p>Choose &#39;Settings&#39; from the main menu. Go to &#39;Switching Desktops&#39;. In &#39;Desktop icons&#39;, please click      the button &#39;Advanced&#39;. The option &#39;Use original Windows desktop folder as Desktop 1&#39; makes Dexpot use your      existing desktop folder instead of creating a new, separate folder for Desktop 1.</p>
      <p class="abstandoben">
        <a name="iconcustomizationshared">
          <span class="control">11. Shared common folder for all desktops</span>
        </a>
      </p>
      <p>Choose &#39;Settings&#39; from the main menu. Go to &#39;Switching Desktops&#39;. Enable the option &#39;Customize desktop      icons&#39;. In &#39;Desktop icons&#39;, please click the button &#39;Advanced&#39;. Enable the option &#39;Use a shared      common folder for all desktops&#39;.
      <br />
      <i>Using this method of icon customization, Dexpot does not touch any of your icons.</i>
      <br />
      <br />Please mind the following characteristics:</p>
      <ul class="presskit">
        <li>To backup the icon distribution, please save a profile via &#39;Settings &gt; General &gt; Profiles&#39;. If necessary,        you can restore the icon distribution using the hotkey &quot;Reload icons from profile&quot; from &#39;Settings &gt;        Controls &gt; Hotkeys &gt; Miscellaneous&#39;</li>
        <li class="fett">You must
        <u>always</u>
        <a href="http://www.dexpot.de/?id=quickstartguide#sharedmoveicons">move</a>,
        <a href="http://www.dexpot.de/?id=quickstartguide#sharedcopyicons">copy</a> and
        <a href="http://www.dexpot.de/?id=quickstartguide#shareddeleteicons">delete</a> icons with Dexpot - not via your Windows        desktop</li>
        <li>Dexpot moves deleted icons and icons that were moved to a different desktop off-screen and therefore hides them</li>
        <li>Applying the Windows functions &quot;Align to grid&quot; as well as &quot;Auto arrange&quot; will gather all icons on        the current desktop. They are redistributed across your desktops within some seconds</li>
        <li>The Dexpot functions &quot;Align to grid&quot; and &quot;Auto arrange icons&quot; just work after a desktop switch and        do not conform to Windows&#39; own functions in every aspect</li>
        <li>Once you quit Dexpot, it will restore the icon distribution that was present before the application was started</li>
      </ul>
      <p class="abstandoben">
        <a name="sharedmoveicons">
          <span class="control">11.1 Moving icons</span>
        </a>
      </p>
      <p>Choose &#39;Desktop Manager&#39; from the main menu. Drag your icon you would like to move to the desired target desktop      in Desktop Manager.
      <br />
      <br />Tip: If you need to move many icons, choose &#39;Desktop Windows&#39; from the main menu. Change to tab      &#39;Icons&#39;. Select the icons you would like to move. Drag the icons to one of the desktops in the left desktop bar.</p>
      <p class="abstandoben">
        <a name="sharedcopyicons">
          <span class="control">11.2 Copying icons</span>
        </a>
      </p>
      <p>Choose &#39;Desktop Manager&#39; from the main menu. While holding CTRL, drag your icon you would like to move to the      desired target desktop in Desktop Manager.
      <br />
      <br />Tip: If you need to copy many icons, choose &#39;Desktop Windows&#39; from the main menu. Change to tab      &#39;Icons&#39;. Select the icons you would like to copy. While holding CTRL, drag the icons to one of the desktops in the      left desktop bar.</p>
      <p class="abstandoben">
        <a name="shareddeleteicons">
          <span class="control">11.3 Deleting icons</span>
        </a>
      </p>
      <p>Choose &#39;Desktop Windows&#39; from the main menu. Change to tab &#39;Icons&#39;. Right-click the icon you would like to      delete, then click &#39;Remove&#39; from the folding menu.</p>
      <p class="abstandoben">
        <a name="aligntogrid">
          <span class="control">11.4 Align icons to grid</span>
        </a>
      </p>
      <p>Choose &#39;Settings&#39; from the main menu. Go to &#39;Switching Desktops&#39;. In &#39;Desktop icons&#39;, please click      the button &#39;Advanced&#39;. The option &#39;Align to grid&#39; aligns your icons to a grid, but after a desktop      switch.</p>
      <p class="abstandoben">
        <a name="autoarrange">
          <span class="control">11.5 Auto arrange icons</span>
        </a>
      </p>
      <p>Choose &#39;Settings&#39; from the main menu. Go to &#39;Switching Desktops&#39;. In &#39;Desktop icons&#39;, please click      the button &#39;Advanced&#39;. The option &#39;Auto arrange icons&#39; aligns your icons left-justified, but after a desktop      switch.</p>
      <p class="abstandoben">
        <a name="desktoprules">
          <span class="control">12. Desktop Rules</span>
        </a>
      </p>
      <p>Choose &#39;Desktop Rules&#39; from the main menu. Press the button &#39;Assistent&#39;. You can choose from a number of      ready-made rules in the rule database. The assistent will guide you through all further steps. After this, you can retouch      the rule where necessary.</p>
      <p class="bisskenabstandoben">
        <img class="siebenfenster" src="http://www.dexpot.de/gfx/rules_en.png" />
      </p>
      <p class="abstandoben">
        <a name="behaviorofwindows">
          <span class="control">13. Behavior of windows on other desktops</span>
        </a>
      </p>
      <p>Choose &#39;Settings&#39; from the main menu. Switch to &#39;Plugins and Extras&#39;. In &#39;Behavior of windows on other      desktops&#39; you can decide how to handle windows activating on another desktop.</p>
