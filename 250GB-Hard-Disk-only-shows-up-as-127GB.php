<h1>250GB Hard Disk only shows up as 127GB</h1>
<br>
You can allocate the rest of the drive using the storage management controls in <br>
Start -&gt; Settings -&gt; Control Panel -&gt; Administrative Tools -&gt; Computer Management -&gt; Computer Management (Local) -&gt; Storage -&gt; Disk Management.<br>
Under here, you should see a chart of your drives and hopefully see a large portion that is allocated. I assume you don't want to lose everything you got now so you should create a new partition on the unallocated space until you are read to do a format. That should
make the space accessible as a new drive letter.
