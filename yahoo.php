<p><span style="font-weight: bold;">Target specific messages to reduce
clutter and time spent searching</span></p>
<p>All search shortcuts work with the minus ("-") modifier, allowing
you to remove specific messages from your search results. </p>
<p><span style="font-weight: bold;">Find messages about a person (or a
company) but not from that person or company</span></p>
<p>Example: Search for messages that mention Laura, minus any that are <span
style="font-style: italic;">from</span> Laura. Type:</p>
<p>laura -from:laura</p>
<p>name(space)(hyphen)from:name </p>
<p><span style="font-weight: bold;">Find messages containing an item,
but not from a specific source</span></p>
<p>Example: Search for ‘Mexico’, minus results from Greg. Type:</p>
<p>mexico -from:greg</p>
<p>(search term)(space)(hyphen)from:source</p>
<p><span style="font-weight: bold;">Messages containing something, and
not containing something else</span></p>
<p>Example: Search for ‘France’ minus any results containing ‘Paris’.
Type:</p>
<p>France -Paris</p>
<p>(search term)(space)(hyphen)(item you want excluded)</p>
<p><span style="font-weight: bold;">Use multiple shortcuts!</span></p>
<p>Combine shortcuts to really pinpoint your searches.</p>
<p><span style="font-weight: bold;">Attachment file types, not from
certain senders</span></p>
<p>Example: Search for jpeg attachments minus any from Cindy. (Stop the
madness, Cindy!) Type:</p>
<p>attachmenttype:jpg -from:cindy</p>
<p>attachmenttype:(file type)(space)(hyphen)from:person</p>
<p>(No offense Cindy! It’s just - Hawaii was great. I get it. OK, yes.
I’m jealous!)</p>
<p><span style="font-weight: bold;">Attachment type, linked to a
message containing a search term</span></p>
<p>Example: Find all messages containing the word you’re searching for
- say, "yellow" - that also have an mpeg movie attached. Type:</p>
<p>yellow attachmenttype:mpeg</p>
