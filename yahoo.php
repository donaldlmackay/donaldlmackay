<h1>Yahoo email shortcuts</h1>
<div id="articleBody" class="article">
<p>Mail’s Search is poised to find anything in your entire account -
messages, attachments, names, words. Here’s how to make the most of it:</p>
<p>See the rectangular window in the upper-left corner under <span
style="font-weight: bold;">Check Mail</span> and <span
style="font-weight: bold;">Compose</span>? With a magnifying-glass
icon inside, and the words "Find messages..." That's the search window.</p>
<p>Click there and type what you're looking for - a name, a word in a
message or attachment, a Subject. If you’re really in the mood to go
turbo, use one of the shortcuts outlined below (smarty!). After you
type, click Go or press Enter.</p>
<p>A new tab opens and lists all the messages containing what you’re
looking for - even results found within attachments. Results show the
sender (From), the Subject, the Date and the Location (folder).
Underneath, they include the text surrounding the word or phrase you're
searching for. The search term itself is highlighted in <span
style="color: green;">green</span>.</p>
<p><span style="font-weight: bold;">Search Shortcuts</span></p>
<p>Here are some tricks for getting exactly the result you want, quick.</p>
<p><span style="font-weight: bold;">Find a phrase</span></p>
<p>Put the phrase in quotes. "Bike for sale". "Desktop application".
"Practice times". Etc. Mail searches for those words - together - in
that order. </p>
