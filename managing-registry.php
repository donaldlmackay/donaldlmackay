<h1>Windows Registry</h1>

<a href="http://en.wikipedia.org/wiki/Windows_Registry">http://en.wikipedia.org/wiki/Windows_Registry</a><br><br>

Windows registry stores all application settings in one logical repository (but a number of discrete files) and in a standardized form.
You can <a href="http://pcsupport.about.com/od/registry/ht/open-registry-editor.htm">use the registry editor</a> regedit.exe (carefully) to modify the windows registry.

