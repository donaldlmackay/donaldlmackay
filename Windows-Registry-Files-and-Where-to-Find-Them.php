<h1>Windows Registry Files and Where to Find Them</h1>
<br>
<a href="http://www.easydesksoftware.com/regfiles.htm">http://www.easydesksoftware.com/regfiles.htm</a><br>
<br>
In Windows XP, 2000, and 2003 there are several Registry files.
These files are named without a file extension and are stored in the
Windows\System32\Config folder. These files are named Software,
System, SAM, Security, Default, and UserDiff. There is one more
Registry file and it does have a file extension, NTuser.dat. In
Windows XP, 2000 and 2003. NTuser.dat is stored in the users folder
under the Documents and Settings folder. Each user has their own
NTuser.dat file. The NTuser.dat file stores all settings that each
user selects; these settings will override settings stored in the
System file.


