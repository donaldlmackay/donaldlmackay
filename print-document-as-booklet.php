<h1>Print document as booklet (brochure) in OpenOffice</h1>
<p class="indent">
<br>
You can print a Writer document as a brochure or a booklet. That is,Writer prints two pages on each side of the paper, so that when you fold the paper, you can read the document as a book.<br><br>
When you create a document that you want to print as a brochure, use portrait orientation for the pages. Writer applies the brochure layout when you print the document.<br>
</p>
<h3>To Print a Brochure</h3>
<ol>
<li>Choose File - Print.</li>
<li>In the Print dialog, click Properties.</li>
<li>In the properties dialog for your printer, set the paper orientation to landscape.</li>
</ol>
<p class="indent">
If your printer prints duplex, and because brochures always print in landscape mode, you should use the "duplex - short edge" setting in your printer setup dialog.<br>
</p>
<ol>
<li>Return to Print dialog, and click Options.</li>
<li>In the Pages area of the Printer Options dialog, select Brochure.</li>
<li>For a printer that automatically prints on both sides of a page, specify to print right pages and left pages.</li>
<li>In the Print dialog, click OK</li>
</ol>
