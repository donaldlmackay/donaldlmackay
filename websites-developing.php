<h3>Transactional Web Sites</h3>
A website which involves a biodirectional flow of data, typically to and  from a database. Customers need to be able to not only look up  information already on a site but, also add new information of their  own. In other words, closely interact and communicate with consumers to  satisfy their wants by providing products/services via an ordering
system.<br>
<br>
To make a website process data coming from a webpage and pass it on to a  database, needs a scripting language. This language will also take data  from the database and use it to create a create dynamic web page containing this data.  The website, the database, and the scripting language need webserver to  run on. All of these need an operating system to run on.<br>
<br>
There are many packages which provide the database, scripting language,  webserver and operating system. A commonly used package is <a href="http://en.wikipedia.org/wiki/LAMP_%28software_bundle%29">LAMP</a>:-
<ul>
  <li>L Linux - the operating system.</li>
  <li>A Apache - the webserver.</li>
  <li>M MySQL - the database.</li>
  <li>P PHP - the scripting language.</li>
</ul>
Variations include <a href="http://en.wikipedia.org/wiki/WAMP">WAMP</a>, a Windows version of LAMP, and <a href="http://www.apachefriends.org/en/index.html">XAMPP</a> a cross platform version of LAMP.<br>
<br>
LAMP or its variations can be installed on your local PC and run and  tested locally before being uploaded to your host service provider.

