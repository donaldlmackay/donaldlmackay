<h1>Using online maps 1:50,000 and 1:25,000 as Pedometer</h1>
<h2>There are 4 choices:</h2>
The Ordnance Survey Landranger (1:50,000), and Explorer (1:25,000) maps
are available online and you can use them to place "drawing pins" and
"string" to mark the route.
<br>There are 4 choices:
<p class="indent">
</p><ol>
<li>the-hug.net     <a class="linkification-ext" href="http://maps.the-hug.net/" title="Linkification: http://maps.the-hug.net/">http://maps.the-hug.net/</a></li>
<li>walkhighlands   <a class="linkification-ext" href="http://www.walkhighlands.co.uk/maps/index.php" title="Linkification: http://www.walkhighlands.co.uk/maps/index.php">http://www.walkhighlands.co.uk/maps/index.php</a></li>
<li>walkhighlands   as above but 1:25,000 map. You need to sign up (free) and then log in.</li>
<li>Ordnance Survey <a class="linkification-ext" href="http://explore.ordnancesurvey.co.uk/" title="Linkification: http://explore.ordnancesurvey.co.uk">http://explore.ordnancesurvey.co.uk</a>. You need to sign up (free) and then log in.</li>
</ol>
<p></p>
<h2>1. maps.the-hug.net</h2>
<a class="linkification-ext" href="http://maps.the-hug.net/" title="Linkification: http://maps.the-hug.net/">http://maps.the-hug.net/</a>
<p class="indent">
</p><ul>
<li>Click on help. Confusingly this comes scrolled 3/4 of the way
through to the end. When you drag the scroll back to the top you see
for example "...enter a postcode, a place name (which can be just about
any name on a 1:50,000 map, not just a town name e.g. 'Ben Nevis' or
'Blackrock Cottage'), or the full Ordnance Survey grid reference..."
Then follow help to produce 1:50,000 map with drawing pins and string.
I created a route as an example:-</li>
<li>"Downton.gif" is the picture of the route taken as a screen snapshot.
<br>See <a class="linkification-ext" href="http://cid-73fe6cc4e90a21ef.office.live.com/self.aspx/files/downton.gif" title="Linkification: http://cid-73fe6cc4e90a21ef.office.live.com/self.aspx/files/downton.gif">http://cid-73fe6cc4e90a21ef.office.live.com/self.aspx/files/downton.gif</a></li>
<li>"Downton.gpx" is the set of "drawing pins".
<br>Download from <a class="linkification-ext" href="http://cid-73fe6cc4e90a21ef.office.live.com/self.aspx/files/downton.gpx" title="Linkification: http://cid-73fe6cc4e90a21ef.office.live.com/self.aspx/files/downton.gpx">http://cid-73fe6cc4e90a21ef.office.live.com/self.aspx/files/downton.gpx</a></li>
<li>If you zoom in past the 1:50,000 map hoping for greater detail you just get a skeletal 1:25,000 map.</li>
<li>From the help, try "Loading a Route" using the Downton.gpx file and then editing it.</li>
</ul>
<p></p>
<h2>2. walkhighlands 1:50,000</h2>
<a class="linkification-ext" href="http://www.walkhighlands.co.uk/maps/index.php" title="Linkification: http://www.walkhighlands.co.uk/maps/index.php">http://www.walkhighlands.co.uk/maps/index.php</a>
<p class="indent">
</p><ul>
<li>As 1. above (<a class="linkification-ext" href="http://maps.the-hug.net/" title="Linkification: http://maps.the-hug.net">http://maps.the-hug.net</a>), walkhighlands produces 1:50,000 map with small circles (not drawing pins - but same effect) and string.</li>
<li>The site was developed by the same team as 1. above.</li>
</ul><p></p>
<h2>3. walkhighlands 1:25,000.</h2>
<ul>
<li>You need to log in. If you don't log in you get the skeletal 1:25,000 map.</li>
<li>As 2. above with the extra detail of 1:25,000. </li>
</ul>
