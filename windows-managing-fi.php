<h1>Windows (7 & Vista) Managing Files</h1>
<h4>Explorer substitutes</h4>
<ul>
<li><a href="http://www.explorerxp.com/">ExploreXP</a> - (freeware). Has the great advantage of folder and file size and is interoperable with Windows Explorer. It can copy and paste, and drag and drop both to and from Windows Explorer.</li>
<li>There are others such as CubicExplorer and xplorer2 lite - but the benefits are not obvious.</li>
<li>There are some circumstances where Windows Explorer (vista) is very slow compared to Windows Explorer (XP) - particularly bulk move and bulk delete from multiple sub folders. A useful tool to overcome this is "<a href="#rename-us">Rename Us</a>" which has move and delete builtin to it.</li>
</ul>
<h4>Searching in Windows 7</h4>
<ul>
<li>By default Windows 7 searches not only for filename, but also for file contents. You may not want this. You can ensure it searches only for filename by <a href="Windows-7-Search.php">using these codes</a>.</li>
</ul>
<h4>Renamers</h4>
<ul>
<li><a href="http://www.albert.nu/Programs/Renamer/default.asp">Renamer6.0</a> - (freeware). Fast to load, supports macros.</li>
<li><a href="http://www.albert.nu/Programs/renamerng/default.asp">RenamerNG</a> - (freeware). Development of Renamer6.0, supports macros with <a href="regex.php">regular expressions</a>, but not as fast as Renamer6.0.</li>
<li><a href="http://www.snapfiles.com/get/flexrenamer.html">Flexible Renamer</a> - (freeware). Can change case, add folder name to filename etc.</li>
<li><a href="http://www.snapfiles.com/get/renameus.html">Rename Us</a> - (freeware). Actually used for batch delete.</li>
</ul>
<h4>Backup &amp; Download</h4>
<ul>
<li><a href="backup.php">Computer backup</a>: automated local or online</li>
<li><a href="http://www.snapfiles.com/get/SyncBack.html">SyncBack</a> - (freeware). Backup to e.g. external hard drive.</li>
<li><a href="http://jdownloader.org/">JDownloader</a> - (freeware). Download management tool.</li>
</ul>
<h4>File and string manipulation utilities</h4>
<ul>
<li><a href="http://www.aplusfreeware.com/categories/util/files/ls.zip">ls</a> - (freeware). Produces a text file which list the files in folder and a lot more.</li>
<li><a href="http://www.hjsplit.org/">hjsplit</a> - (freeware). Breaks a file up into pieces. Either by number of lines or bytes.</li>
<li><a href="http://www.clrsoftware.com/tabstospaces/">Tabs To Spaces (CLR)</a> - (shareware). Converts Tabs To Spaces. Useful for pasting an extracted spreadsheet into a text file and maintaining columnar layout</li>
<li><a href="winxs.php">WinXs</a> - (shareware). Unix style file and string manipulation utilities for Windows.</li>
</ul>
<h4>Misc</h4>
<ul>
<li><a href="http://download.cnet.com/PowerArchiver-2002/3000-2250_4-10191499.html">PowerArchiver-2002</a> - (shareware). Compresses to zip or unpacks from zip, lots of files in one shot</li>
<li><a href="http://www.snapfiles.com/get/treecomp.html">TreeComp</a> - (freeware). Compare 2 directory trees, show differences and optionally synchronize the files within the directories.
</li>

