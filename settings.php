<h3>Replay AV<br>
</h3>
Replay AV schedules (i.e. list of radio programs and when they are
to be recorded) are stored in Schedule8.dat. Put <a href="https://skydrive.live.com/?cid=73fe6cc4e90a21ef&amp;id=73FE6CC4E90A21EF%211654#cid=73FE6CC4E90A21EF&amp;id=73FE6CC4E90A21EF%211046">Schedule8.dat</a>.
into "C:\Program Files\Replay AV 8". An example Schedule8.dat is
available from the <a  href="https://skydrive.live.com/?cid=73fe6cc4e90a21ef&amp;id=73FE6CC4E90A21EF%211623#cid=73FE6CC4E90A21EF&amp;id=73FE6CC4E90A21EF%211626">skydrive objects</a> webpage and click on "Schedule8.dat".<br>
In order to sort the resulting mp3 files into date and time order,
the Replay AV filename needs to have the format: %Y-%m-%d-%%s i.e:
yyyy-mm-dd-filename. This is set in Replay AVsettings - Other tab
- file naming format.
