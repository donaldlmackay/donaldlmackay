<h3>Other Details</h3>
<p>1. When sending a file (e.g. picture) from android, find the "share" icon. It looks like this:-
</br><a href="https://t4.ftcdn.net/jpg/01/07/62/09/500_F_107620951_3khjilVjDc6RtdmBQL417guwZS6UNATH.jpg">https://t4.ftcdn.net/jpg/01/07/62/09/500_F_107620951_3khjilVjDc6RtdmBQL417guwZS6UNATH.jpg</a>
</br>
only smaller. It is usually on the top or bottom of the screen with various other icons (depends on manufacturer, model, version of android etc). Clicking on it, one of the choices is bluetooth
</br><a href="https://upload.wikimedia.org/wikipedia/commons/c/ca/Crystal_Project_bluetooth.png">https://upload.wikimedia.org/wikipedia/commons/c/ca/Crystal_Project_bluetooth.png</a>
</p>
<p>2. When receiving a file on android, it goes into a folder called either "bluetooth", "download" or something similar usually on the phone's internal memory, not on the SD card (if there is one).
</p>
