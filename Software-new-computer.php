<h1>Software for new computer</h1>
<br>
1. Pre-installed junk<br>
I have always got rid of most of this nag-ware. It gives you say 30 days
 or 10 uses, or some limited version that you usually don't need e.g
Symantec Norton in 1 below.<br>
<br>
Symantec Norton Internet Security. Until fairly recently, I was a great
enthusiast for AVG. Some while ago I heard about "Microsoft Security
Essentials" (MSE) http://www.microsoft.com/security_essentials/. I
downloaded and installed it. I also kept AVG. To my surprise they both
worked independently of each other. I had thought they would interfere,
and was surprised to have no problems.<br>
<br>
However this did not last. When AVG got more and more insistent that I
upgraded to AVG 2011 (or some such), I eventually gave in and tried the
upgrade. It failed because it objected to MSE. At this point I had to
make a decision. I don't remember the details of why I chose MSE vs AVG,
 but I probably googled and found people suggested ditching AVG. However
 the Dell (equivalent spec to the Compaq) has both MSE and AVG, with
no problems.<br>
<br>
I also have "Spybot - Search &amp; Destroy" and "Malwarebytes'
Anti-Malware". I run all 3 (these 2 and MSE) on any new software and so
far so good. All 3 seem to play together well.<br>
<br>
