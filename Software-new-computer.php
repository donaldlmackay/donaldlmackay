5. Why not use widely known and used software instead or what is
provided by Windows. I use VLC. "Sent: Fri, 14 January, 2011 20:35:48
Subject: VLC (was: IT) ... The media player to use these days is VLC
http://www.videolan.org/vlc/.<br>
<br>
I use it for everything audio and video. It occupies 76MB on my laptop. I
 used it yesterday to play a .mov file. Don't be tempted to go for
Apple's Quick Time or iTunes. Quick Time was originally the only way to
play .mov. Then iTunes came along and had Quick Time incorporated. I
have given up on Quick Time but still use iTunes to download podcasts.
iTunes and Quick Time have bloated as Apple keep on adding more features
 onto them..."<br>
<br>
6. Microsoft Office Starter: Don't know this. The HP and the Dell, came with:- <br>
a) Microsoft Office (MO) full version (nag-ware)<br>
b) Microsoft Works - a full office suite but limited in comparison to
MO. I think you would find the restrictions irksome if use to full MO.<br>
c) Microsoft Wordpad - a pathetic word processor I blunder into and regret dealing with.<br>
<br>
So I installed OpenOffice. I think you may have this on the expiring
Fujitsu. I like and use this all the time. I use v3.2 not a later
version. They changed one of the settings I used for printing booklets.
So I reverted back to v3.2.<br>
